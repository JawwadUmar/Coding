int MOD = 1e9+7;
int binaryExponentiation(int x, int p){
    int res = 1;
    while(p){
        if(p%2){
            res = (res * x)%MOD;
        }

        x = (x*x)%MOD;
        p = p/2;
    }
    
    return res;
}

int josephus(int n, int k){
    
    if(n ==0){
        return 0;
    }

    return (josephus(n-1, k) + k)%(n);
}
